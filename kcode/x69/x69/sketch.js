function Grevious(lx,ly,rot,sc, fade) {
    push();
    translate(lx,ly);
    rotate(radians(rot));
    scale(sc);
    for (var i = 0; i < biggergridarr.length; i++) {
        for (var j = 0; j < biggergridarr[0].length; j++) {
             let value = biggergridarr[i][j];
              if ( value == 0 ){
                  fill(0,0,0, fade);
              } else if ( value == 1 ){
                  fill(255, fade);
              } else if (value == 2){
                  fill(127, fade);
              } else {
                  fill(150,150,0, fade);
              }
            rect(j * 12, i * 12, 10, 10);
        }
    }
    pop();

}


